			<h2>Content Strategy</h2>
				<p><strong>Main Menu: </strong>Landing page for the entire website, first impression, and primary source for introducing direction into navigation of the website as a whole. Provides a brief blurb before directing users towards the 'Stats', or 'Adventures' pages for more information.</p>
				<p><strong>Stats: </strong>Contains the typical information you would expect from an About Me page--an overview of past education, professional history, skillbase, and other miscellanea. The ability to download a full resume is provided here.  Also contains the contact form for those interested in me professionally, or those who would like more information about myself or past projects. Provides links for professional social networking pages, like LinkedIn, and GitHub, though those are linked as icons in the sidebar as well.  The overall tone should be personable and relaxed but not overly colloquial. </p>
				<p><strong>Adventures: </strong> This page contains a gallery of professional and personal projects that are worth highlighting.  It utilizes an interactive slideshow format in order to maximize impact.  Each project will feature an image and then a short project description, including links to external webpages, or other downloadable project material. </p>
